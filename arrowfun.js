//arrow functions are used for writing a function in a shorter or consice way(more readable and structured)
//return statment and curly braces are optional for mentioning
//Generic way 
let x;
x = function(){
    console.log("hello");
}
//arrow way (without parameters)
x = () => {
    console.log("hello");
}
//with parameters
x = (a,b) => {
    console.log("hello");
}


